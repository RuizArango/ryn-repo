class Potion:
    def __init__(self, name, effect_type, effect_amount, price):
        self.name = name
        self.effect_type = effect_type
        self.effect_amount = effect_amount
        self.price = price

    def __str__(self):
        return f"{self.name} ({self.effect_type}: {self.effect_amount}, Price: ${self.price})"

    def use(self, player):

        # Heal
        if self.effect_type == "heal":
            player.health += self.effect_amount

            if player.health > player.max_health:
                player.health = player.max_health

            print(f"{player.name} healed for {self.effect_amount} HP!")
            print(f"Current Health: {player.health}/{player.max_health}")

        # Damage boost
        elif self.effect_type == "damage":
            player.damage_boost += self.effect_amount

            print(f"{player.name}'s damage increased by {self.effect_amount}!")

        # Max hp
        elif self.effect_type == "max_health":
            player.max_health += self.effect_amount
            player.health += self.effect_amount

            print(f"{player.name}'s max health increased by {self.effect_amount}!")
            print(f"New Max Health: {player.max_health}")

