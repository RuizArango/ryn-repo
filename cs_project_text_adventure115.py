#Shop
def display_shop(weapons, potions):

    print("\n*** Welcome to the Shop! ***")

    print("\nWEAPONS:")
    for i, weapon in enumerate(weapons):
        print(f"{i + 1}. {weapon}")

    potion_start = len(weapons)

    print("\nPOTIONS:")
    for i, potion in enumerate(potions):
        print(f"{potion_start + i + 1}. {potion}")

    print(f"\n0. Exit Shop")


def run_shop(player, shop_weapons, shop_potions):

    while True:

        player.display_status()

        display_shop(shop_weapons, shop_potions)

        try:
            choice = int(input("\nChoose an item to buy: "))

            # Exit shop
            if choice == 0:
                print("\nExiting shop.")
                break

            # Weapon purchases
            elif 1 <= choice <= len(shop_weapons):

                chosen_weapon = shop_weapons[choice - 1]
                player.buy_weapon(chosen_weapon)

            # Potion purchases
            elif len(shop_weapons) < choice <= len(shop_weapons) + len(shop_potions):

                potion_index = choice - len(shop_weapons) - 1
                chosen_potion = shop_potions[potion_index]

                player.buy_potion(chosen_potion)

            else:
                print("Invalid choice.")

        except ValueError:
            print("Please enter a number.")

