#Hello World
class Weapon:
    def __init__(self, name, damage, price):
        self.name = name
        self.damage = damage
        self.price = price

    def __str__(self):
        return f"{self.name} (Damage: {self.damage}, Price: ${self.price})"

class Player:
    def __init__(self, name, starting_money=100, starting_health=100):
        self.name = name
        self.money = starting_money
        self.inventory = []
        self.max_health = starting_health
        self.health = starting_health
        self.damage_boost = 0
        self.potions = []

    def display_status(self):
        print(f"\n{self.name}'s Status:")
        print(f"  Money: ${self.money}")
        print(f"  Inventory: {[weapon.name for weapon in self.inventory] or 'Empty'}")
        print(f"  Health: {self.health}/{self.max_health}")
        print(f"  Damage Boost: {self.damage_boost}")
        print(f"  Potions: {[potion.name for potion in self.potions] or 'Empty'}")

    def buy_weapon(self, weapon):
         # Prevent duplicate purchases
      if weapon in self.inventory:
        print(f"You already own {weapon.name}.")
        return

    # Check if player has enough money
      if self.money >= weapon.price:
        self.money -= weapon.price
        self.inventory.append(weapon)

        print(f"{self.name} bought {weapon.name} for ${weapon.price}.")

      else:
        print(f"{self.name} does not have enough money to buy {weapon.name}.")

    def buy_potion(self, potion):

      if self.money >= potion.price:
          self.money -= potion.price
          self.potions.append(potion)

          print(f"{self.name} bought {potion.name}.")

      else:
          print("Not enough money.")

class Monster:
    def __init__(self, name, health, damage, money_drop):
        self.name = name
        self.health = health
        self.damage = damage
        self.money_drop = money_drop

    def __str__(self):
        return f"{self.name} (Health: {self.health}, Damage: {self.damage}, Money Drop: ${self.money_drop})"

    def take_damage(self, amount):
        self.health -= amount
        if self.health < 0:
            self.health = 0
        print(f"{self.name} took {amount} damage! {self.name}'s health: {self.health}")

    def is_defeated(self):
        return self.health <= 0

